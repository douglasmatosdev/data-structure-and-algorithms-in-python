# 1. Ler dois números inteiros, executar e mostrar o resultado das seguintes operações: adição, subtração, multiplicação e divisão
print('Digite apenas números inteiros!')
a = int(input('Digite um valor para a: '))
b = int(input('Digite um valor para b: '))

print(f'a = {a} e b = {b}')
print(f'A resultado da soma de a + b = {a+b}')
print(f'A resultado da subtração de a - b = {a-b}')
print(f'A resultado da divisão de a / b = {a/b}')
print(f'A resultado da multiplicação de a * b = {a*b}')
